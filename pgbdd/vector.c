#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdarg.h>
#include <stdnoreturn.h>

#include "vector.h"

void vector_error(const char *fmt,...)
{
    va_list ap;
    fprintf(stderr,"vector_error:");
    va_start(ap, fmt);
    vfprintf(stderr, fmt, ap);
    va_end(ap);
    putc('\n', stderr);
    exit(EXIT_FAILURE);
}
